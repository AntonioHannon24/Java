package Projeto_veiculo;

public class Veiculo {
	
	private String Marca,Ano,Cor; boolean Vendido;
	private double Preco,potMotor;
	
	public String getMarca() {
		return this.Marca;
	}
	public void setMarca(String marca) {
		this.Marca = marca;
	}
	
	public String getAno() {
		return this.Ano;
	}
	public void setAno(String an) {
		this.Ano = an;
	}
	
	public String getCor() {
		return this.Cor;
	}
	public void setCor(String cor) {
		this.Cor = cor;
	}
	public boolean getVendido() {
		return this.Vendido;
	}
	public void setVendido(boolean ven) {
		this.Vendido = ven;
	}
	public double getPreco() {
		return this.Preco;
	}
	public void setPreco(double pre) {
		this.Preco = pre;
	}
	public double potMotor() {
		return this.potMotor;
	}
	public void setPotMotor(double pot) {
		this.potMotor = pot;
	}
	
	
	
	
	
	
	
	
	

}
