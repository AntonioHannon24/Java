public class Missao_principal {

	public static void main(String[] args) {
		
		System.out.println("Inicio da missão 1");
		float num1,num2,resultado; // iniciando as variaveis 
		
		num1 = 10;  // a variavel num1 recebe o valor 10
		num2=60;    // a variavel num2 recebe o valor 60
		resultado = num1+num2; // soma entre as variaveis num1 e num2 sendo depositadas em resultado
		
		
		
		
		System.out.print("O resultado da soma entre "+num1+" e "+num2+" é " + resultado);
	}

}
